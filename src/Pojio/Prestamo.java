package Pojio;

public class Prestamo {
    private String libro;
    private int dia;
    private String mes;

    public String getLibro() {
        return libro;
    }

    public int getDia() {
        return dia;
    }

    public String getMes() {
        return mes;
    }

    public void setLibro(String libro) {
        this.libro = libro;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }
}
